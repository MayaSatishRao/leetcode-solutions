class Solution
{
    //Function to find the smallest positive number missing from the array.
    static int missingNumber(int arr[], int size)
    {
        // Your code here
        TreeSet<Integer> ts = new TreeSet<>();
        int i=0;
        while(i<arr.length){
            if(arr[i]>0)
              ts.add(arr[i]);
            i++;
        }
        i=0;
        int curr=1;
        for(int j:ts){
            if(j!=curr)
              return curr;
            curr++;
        }
        return curr;
    }
}


