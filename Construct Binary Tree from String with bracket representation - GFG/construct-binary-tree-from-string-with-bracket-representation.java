class Solution {
    public static Node treeFromString(String s) {
        // code here
        
        int i=0;
        for(;i<s.length();i++)
           if(s.charAt(i)>='0' && s.charAt(i)<='9')
             continue;
           else
              break;
        Node root = null;
        
        if(i>0)
         root = new Node(Integer.parseInt(s.substring(0,i)));
        else
        return null;
        
        int open=1;
        int leftStart=i+1,j=i+2;
        while(j<s.length() && open!=0){
            if(s.charAt(j)=='(')
              open++;
            else if(s.charAt(j)==')')
              open--;
              j++;
        }
        if(leftStart<s.length())
          root.left = treeFromString(s.substring(leftStart,j));
        int rightStart=j+1;
        if(rightStart<s.length())
          root.right=treeFromString(s.substring(rightStart));
        
        return root;
    }
}
        
